using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Ejercicio8
{
    class Program
    {
        static void Main(string[] args)
        {
            /* 8. Solicite al usuario el ingreso de una frase y presente un mensaje indicando si 
             alguna contiene la palabra "fin". */
            string frase;
            Console.WriteLine("Ingrese una frase");
            frase = Console.ReadLine();
            foreach()
            {
                frase.Contains("fin");
                Console.WriteLine($"La {frase} contiene fin");
            }
          
        }
    }
}
